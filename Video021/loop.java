public class loop {
    public static void main(String[] args) {
        int j = 1;
        while(j<=10){
            System.out.println(j);
            j++;
        }


        // Quick Quiz using for loop:
        for(int i = 100;i<201;i++){
            System.out.println(i);
        }

        // Quick Quiz using while loop:
        int k = 100;
        while(k<=200){
            System.out.println(k);
            k++;
        }
    }
}